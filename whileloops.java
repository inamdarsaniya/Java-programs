public class whileloops {
    public static void main(String[] args) {
        System.out.println("while loop");
        int i=10;
        while(i!=0) {
            System.out.println(i);
            i--;
        }
        System.out.println("do while loop");
        do {
            System.out.println(i);
        }while(i>1);
    }
}
